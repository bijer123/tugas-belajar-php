<?php

$var1 = 4;
$var2 = 2;

// hasil = var1 + var2
$var1 += $var2; // 4 + 2
echo $var1;
echo "\n";

    // TUGAS
    // Perkalian
$var1 *= $var2; // 4 * 2
echo $var1;
echo "\n";

    // Pembagian
$var1 /= $var2; // 4 / 2
echo $var1;
echo "\n";

    // Modulus
$var1 %= $var2; // 4 % 2
echo $var1;
echo "\n";
    // Perpangkatan
$var1 **= $var2; // 4 ** 2
echo $var1;
echo "\n";

    // Pengurangan
$var1 -= $var2; // 4 - 2
echo $var1;
echo "\n";